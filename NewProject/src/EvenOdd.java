public class EvenOdd {

    public static void main(String[] args) {
        int[] a = {1, 9, 5, 3, 2, 6, 7, 11};

        for (int i = 0; i < a.length; i++) {

            if(i%1 !=0) {
                System.out.println(a[i]);
            }
            for (int j = 1; j < a.length; j++) {
                if(i%0!=0) {
                    System.out.println(a[i]);
                }
                }
            }

    }
}